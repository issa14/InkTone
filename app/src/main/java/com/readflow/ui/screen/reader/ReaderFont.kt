package com.readflow.ui.screen.reader

/**
 * Police de lecture disponible dans le panneau de réglages visuels.
 */
enum class ReaderFont {
    /** Police avec empattements (Serif) — lecture classique. */
    SERIF,
    /** Police sans empattements (Sans-Serif) — lecture moderne. */
    SANS_SERIF,
    /** Police OpenDyslexic — adaptée aux lecteurs dyslexiques. */
    OPEN_DYSLEXIC
}
